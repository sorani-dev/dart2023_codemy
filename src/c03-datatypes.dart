void main() {
  /**
   * Data Types:
   * Number, String, Boolean, List, Map, Dynamic
   */

  // String
  String firstName = "Simon";
  print("String: $firstName");

  // Integer
  int myNum = 42;
  print("Integer: $myNum");

  // Double
  double myDouble = 3.1459;
  print("Double: $myDouble");

  // Boolean
  bool canVote = true;
  bool canDrive = false;
  print("Boolean: Can vote: $canVote");
  print("Boolean: Can drive: $canDrive");

  // Dynamic
  dynamic fullName = "John Doe";
  print("Dynamic: $fullName");
}
/**
 * Output:
 * 
 * 
String: Simon
Integer: 42
Double: 3.1459
Boolean: Can vote: true
Boolean: Can drive: false
Dynamic: John Doe
 */
